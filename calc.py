def add (a, b):
  add = a + b
  return add
print(add(3, 4))

def subtract (a, b):
  subtract = a - b
  return subtract
print(subtract(3, 4))

def multiply (a, b):
  multiply = a * b
  return multiply
print(multiply(3, 4))

def divide (a, b):
  divide = a / b
  return divide
print(divide(3, 4))

def exp (a, b):
  exp = a ** b
  return exp
print(exp(3, 4))